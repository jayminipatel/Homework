package loopsswitchcase;

public class WhileLoop {
    public static void main(String[] args) {
        //print 10 even number
        int i=0;
        while (i<=20){
            System.out.println(i);
            i+=2;
        }
        //print 10 odd numbers
        int g=1;
        while (g<=20){
            System.out.println(g);
            g+=2;
        }

        //print table 9
        int j=1;
        while (j<=10){
            System.out.println("9 *" +j+"=" + 9*j);
            j++;
        }



    }
}





